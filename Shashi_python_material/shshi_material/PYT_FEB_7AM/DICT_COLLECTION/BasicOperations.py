
stu={}

#adding an item to dict
stu['sno']=101
stu['sname']='Ramesh'
print("Dict : ",stu)
print("=============")

#Updating an existed item
stu['sname']="Suresh"
print("Dict : ",stu)
print("=============")

#Reading value of Dict
name=stu['sname']
print("Name is : ",name)
print("=============")

#deleting an item
del stu['sname']
print("Dict : ",stu)










