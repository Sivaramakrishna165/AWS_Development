




''' Membership operator
       in : Returns True iff Specified
                         Object is existed in the iterable
                               Syn: object in iterable
                               
       not in :
          Returns True iff Specified Object is not existed
              in the iterable
                         Syn: object not in iterable
'''

lst=[10,20,30,40,50]
print("List : ",lst)
print("30 in lst ? : ",30 in lst)
print("90 in lst ? : ",90 in lst)
print("====================")

print("90 not in lst  ? : ",90 not in lst)






















    
