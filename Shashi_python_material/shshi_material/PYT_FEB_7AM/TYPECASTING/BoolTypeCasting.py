#bool type casting using bool( )
#int -> bool
x=-123
y=bool(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

#float -> bool
x=0.0
y=bool(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

#complext -> bool
x=0+0j
y=bool(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

