# string type casting using str( )

#int -> str
x=10
y=str(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

#float -> str
x=12.1212
y=str(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

#complext -> str
x=10+20j
y=str(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")

#bool -> str
x=True
y=str(x)
print(type(x)," ----> ",type(y))
print("x : ",x," ----> ","y : ",y)
print("=====================")
















