



'''
Conditional Operator
In Other Languages C and C++
      (<condition>)?Logical_True:Logical_False;

In Python
Logical_True if condition else Logical_False
'''
marks=9
print("Pass") if marks>34 else print("Fail")
print("===============================")

age=28
print("Elig") if age>=18 else print("Not Elig")
print("=================================")

x=False
print("Hello") if not x else print("Nice")



















