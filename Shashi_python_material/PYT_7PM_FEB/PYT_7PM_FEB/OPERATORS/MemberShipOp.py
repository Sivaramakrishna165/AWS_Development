


''' Membership Operators
      in : Returns True iff specified
                Object is existed in the collcection

                    Syn: object in collection
                    
      not in: Returns True iff the specified Object is
      Not Existed in the collection
      
                    Syn: object not in collection      
'''
lst=[10,20,30,40,50,60]
print("List : ",lst)
print("30 in lst ? : ", 30 in lst)
print("90 not in lst ? : ",90 not in lst)
print("=========================")

