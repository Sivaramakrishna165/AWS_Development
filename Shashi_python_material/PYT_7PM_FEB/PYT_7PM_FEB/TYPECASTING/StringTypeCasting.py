
''' str type casting using str( ) -> str '''
x=123
y=str(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")

x=12.12
y=str(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")

x=(10+20j)
y=str(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")

