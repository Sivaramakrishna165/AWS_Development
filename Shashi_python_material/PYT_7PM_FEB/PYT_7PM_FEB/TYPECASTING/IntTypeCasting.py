
''' int type casting using int( ) -> int '''
#float -> int
x=12.12
y=int(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")

#complex -> int
x=(10+20j)
#y=int(x) TypeError

#str -> int
x="12"
y=int(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")

#bool -> int
x=False
y=int(x)
print(type(x)," ----> ",type(y) )
print(x," -----> ",y)
print("=====================")








