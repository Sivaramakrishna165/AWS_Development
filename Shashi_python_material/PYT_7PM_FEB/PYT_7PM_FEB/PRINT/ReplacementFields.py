
''' Working with Replacement Fields :

    * Replacement Fields are rep by using {}
    * while working with replacement fields
       we have to use format( ) method from str class.

    * Replacement Fields are 2 types
                * Automatic Field numbering System
                * Manual Field Specifications
                        1.Index based
                        2.name based

    Syn: print("replacement fields".format(variables))
'''

x=10
print("{}".format(x))
print("x val is : {}".format(x))

y=12.12
print("{}{}".format(x,y))
print("x val is :{} and y val is : {}".format(x,y))









           
       







